void Mesh::draw() const
{
	glBindVertexArray(vertexArrayObject_);
	glBindTexture(GL_TEXTURE_2D, textureID_);
	glPolygonMode(GL_FRONT_AND_BACK, GL_FILL);
	glDrawArrays(GL_TRIANGLES, 0, numberOfVerticies_);
}


